    private void carIdFieldKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_carIdFieldKeyPressed

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            try {
                Class.forName("com.mysql.jdbc.Driver");
                con = DriverManager.getConnection("jdbc:mysql://localhost/car_rent", "root", "Lahiru@448");

                String car_id = carIdField.getText();

                pst = con.prepareStatement("select car_id,cust_id,due,DATEDIFF(NOW(),due) as elap from rental where car_id =?");
                pst.setString(1, car_id);

                resultSet = pst.executeQuery();

                if (resultSet.next() == false) {
                    JOptionPane.showMessageDialog(this, "Car ID not found");
                } else {
                    String cust_id = resultSet.getString("cust_id");
                    custIdField.setText(cust_id.trim());

                    String date = resultSet.getString("due");
                    dateField.setText(date.trim());

                    String elp = resultSet.getString("elap");
                    int elaped = Integer.parseInt(elp);

                    if (elaped > 0) {
                        daysField.setText(elp);

                        int fine = elaped * 100;
                        fineField.setText(String.valueOf(fine));

                    } else {

                        daysField.setText("0");
                        fineField.setText("0");
                    }

                }

            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Return.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(Return.class.getName()).log(Level.SEVERE, null, ex);
            }

        }

    }//GEN-LAST:event_carIdFieldKeyPressed

