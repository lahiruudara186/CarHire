public void table_update() {

        int columnCount;

        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/car_rent", "root", "Lahiru@448");
            pst = con.prepareStatement("select * from return_car");
            ResultSet resultSet = pst.executeQuery();

            ResultSetMetaData resultsetMetadata = resultSet.getMetaData();
            columnCount = resultsetMetadata.getColumnCount();

            DefaultTableModel defaultTableModel = (DefaultTableModel) jTable1.getModel();
            defaultTableModel.setRowCount(0);

            while (resultSet.next()) {

                Vector vector = new Vector();
                for (int i = 1; i <= columnCount; i++) {
                    vector.add(resultSet.getString("car_id"));
                    vector.add(resultSet.getString("cust_id"));
                    vector.add(resultSet.getString("return_date"));
                    vector.add(resultSet.getString("elap"));
                    vector.add(resultSet.getString("fine"));
                }
                defaultTableModel.addRow(vector);

            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(CarRegistration.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(CarRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
